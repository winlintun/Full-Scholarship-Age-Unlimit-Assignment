
let variable = 'car';


switch (variable) {
	case 'car':
		console.log("This is a car");
		break;
	case 'bike':
		console.log("This is a bike");
		break;
	case 'boat':
		console.log("This is a boat");
		break;
	case 'bicycle':
		console.log("This is a bicycle");
		break;
	default:
		console.log("none of this.")
		break;
}

